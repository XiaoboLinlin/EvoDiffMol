def get_dataset_info(dataset_name, remove_h):
    if dataset_name == 'qm9':
        if not remove_h:
            return qm9_with_h
        else:
            return qm9_without_h
    elif dataset_name == 'qm40':
        if not remove_h:
            return qm40_with_h
        else:
            return qm40_without_h
    elif dataset_name == 'geom':
        if not remove_h:
            return geom_with_h
        else:
            raise Exception('Missing config for %s without hydrogens' % dataset_name)
    elif dataset_name == 'crossdock':
        if not remove_h:
            return crossdock_with_h
        else:
            raise Exception('Missing config for %s without hydrogens' % dataset_name)
    elif dataset_name == 'qm9_second_half':
        if not remove_h:
            return qm9_second_half
        else:
            raise Exception('Missing config for %s without hydrogens' % dataset_name)
    elif dataset_name == 'guacamol':
        if not remove_h:
            return guacamol_with_h
        else:
            return guacamol_without_h
    elif dataset_name == 'moses':
        if not remove_h:
            return moses_with_h
        else:
            return moses_without_h
    else:
        raise Exception("Wrong dataset %s" % dataset_name)
